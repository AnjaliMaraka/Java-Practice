package com.aarogya.Dao;

public class Patient {
	private int id;
	private String name;
	private String gender;
	private String city;
	private int age;
	private long aadharcard;
	private long contact;
	private String address;
	private String dateofaddmission;
	private String g_name;
	private String g_aadress;
	private long g_contact;
	private String p_recovery;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public long getAadharcard() {
		return aadharcard;
	}
	public void setAadharcard(long aadharcard) {
		this.aadharcard = aadharcard;
	}
	public long getContact() {
		return contact;
	}
	public void setContact(long contact) {
		this.contact = contact;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getDateofaddmission() {
		return dateofaddmission;
	}
	public void setDateofaddmission(String dateofaddmission) {
		this.dateofaddmission = dateofaddmission;
	}
	public String getG_name() {
		return g_name;
	}
	public void setG_name(String g_name) {
		this.g_name = g_name;
	}
	public String getG_aadress() {
		return g_aadress;
	}
	public void setG_aadress(String g_aadress) {
		this.g_aadress = g_aadress;
	}
	public long getG_contact() {
		return g_contact;
	}
	public void setG_contact(long g_contact) {
		this.g_contact = g_contact;
	}
	public String getP_recovery() {
		return p_recovery;
	}
	public void setP_recovery(String p_recovery) {
		this.p_recovery = p_recovery;
	}
	
	
}
